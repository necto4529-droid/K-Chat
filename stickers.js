// ── ФОЛЛБЭК ДЛЯ "СЛИШКОМ НОВЫХ" ЭМОДЗИ ──
// Некоторые эмодзи (Unicode 16.0+, 2024-2025) ещё не поддерживаются
// системными шрифтами на многих устройствах и рендерятся "пусто".
// Для них показываем картинку из Twemoji (jdecked-форк, поддерживает
// все актуальные эмодзи) вместо текстового символа.
// Ключ — сам эмодзи, значение — codepoint(ы) в hex через дефис
// (без variation selector FE0F и ZWJ 200D — как требует Twemoji SVG).
// ══════════════════════════════════════════════════════════════════
const STICKER_IMG_FALLBACK={
  '🫩':'1fae9', // лицо с мешками под глазами (Unicode 16.0, 2024)

  // ── Unicode 16.0 (сентябрь 2024) ──
  '🫆':'1fac6',         // отпечаток пальца
  '🫟':'1fadf',         // брызги/клякса
  '🫜':'1fadc',         // корнеплод
  '🪾':'1fabe',         // голое дерево без листьев
  '🪉':'1fa89',         // арфа
  '🪏':'1fa8f',         // лопата
  '🇨🇶':'1f1e8-1f1f6',  // флаг Сарк (regional indicators C+Q)

  // ── Unicode 17.0 (сентябрь 2025) ──
  '🫪':'1faea', // искажённое лицо
  '🫯':'1faef', // облако драки
  '🫍':'1facd', // косатка (орка)
  '🫈':'1fac8', // волосатое существо (йети/бигфут)
  '🪊':'1fa8a', // тромбон
  '🛘':'1f6d8', // обвал/оползень
  '🪎':'1fa8e', // сундук с сокровищами
};

const TWEMOJI_SVG_BASE='https://cdn.jsdelivr.net/gh/jdecked/twemoji@latest/assets/svg/';

// Универсальная отрисовка стикера: текстовый эмодзи ИЛИ <img> для
// неподдерживаемых системой эмодзи. container — DOM-элемент, в который
// нужно вставить визуал (его текущее содержимое будет заменено).
function renderStickerVisual(container,emoji){
  const code=STICKER_IMG_FALLBACK[emoji];
  if(code){
    container.textContent='';
    const img=document.createElement('img');
    img.src=TWEMOJI_SVG_BASE+code+'.svg';
    img.alt=emoji;
    img.draggable=false;
    img.className='sticker-fallback-img';
    // Если картинка не загрузилась (нет интернета и т.п.) — fallback на текст
    img.onerror=()=>{container.textContent=emoji;};
    container.appendChild(img);
  }else{
    container.textContent=emoji;
  }
}
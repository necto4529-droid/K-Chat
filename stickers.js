// Недавние стикеры — хранятся в localStorage
const RECENT_MAX = 24;
function getRecentStickers(){
  try{return JSON.parse(localStorage.getItem('bc_recent_stickers')||'[]');}catch(e){return[];}
}
function addRecentSticker(emoji){
  let rec=getRecentStickers();
  rec=rec.filter(s=>s!==emoji);
  rec.unshift(emoji);
  if(rec.length>RECENT_MAX)rec=rec.slice(0,RECENT_MAX);
  try{localStorage.setItem('bc_recent_stickers',JSON.stringify(rec));}catch(e){}
}

